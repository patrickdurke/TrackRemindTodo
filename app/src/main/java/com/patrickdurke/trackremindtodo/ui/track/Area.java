package com.patrickdurke.trackremindtodo.ui.track;

public class Area {
    private int id;
    private String name;

    public Area() {
    }

    public Area(String name) {
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

}
